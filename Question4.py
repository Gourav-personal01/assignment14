# SOAP is a protocol for communication between applications. SOAP supports only XML data exchange. SOAP is useful in legacy applications and private APIs.
# REST is an architecture style for designing communication interfaces. REST is useful in modern applications and public APIs

# Shortcomings of SOAP 
# 1. API Calls are not Cached. An API call is a process of sending a request when an API has been set up with the necessary endpoints.
# 2. Extremely Complicated. SOAP is substantially more sophisticated than REST